-- Compare-and-delete: завершает раунд ровно один раз.
-- Удаляет ключ раунда только если его токен совпадает с ожидаемым.
-- Одна атомарная операция Redis закрывает гонку "таймер vs все ответили".
-- KEYS[1] = ключ раунда (game:{chat}:round)
-- ARGV[1] = ожидаемый токен
-- Возврат: 1 если этот вызов завершил раунд, 0 если раунд уже завершён/сменился.
if redis.call('GET', KEYS[1]) == ARGV[1] then
    return redis.call('DEL', KEYS[1])
end
return 0
